			function DaysArray(n) {
			    for (var i = 1; i <= n; i++) {
			        this[i] = 31
			        if (i == 4 || i == 6 || i == 9 || i == 11) {
			            this[i] = 30
			        }
			        if (i == 2) {
			            this[i] = 29
			        }
			    }
			    return this
			}
			
